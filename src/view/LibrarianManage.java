    private void tableMainMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMainMouseClicked
        // TODO add your handling code here:
        clearData();
        int selectedRow = tableMain.getSelectedRow();
        if (selectedRow != -1) {
            txtMaTT.setText(tableMain.getValueAt(selectedRow, 2).toString());
            txtUserID.setText(tableMain.getValueAt(selectedRow, 0).toString());
            if(tableMain.getValueAt(selectedRow, 3) != null && !tableMain.getValueAt(selectedRow, 3).toString().equals(""))
                txtHoTen.setText(tableMain.getValueAt(selectedRow, 3).toString());
            if(tableMain.getValueAt(selectedRow, 4) != null && !tableMain.getValueAt(selectedRow, 4).toString().equals(""))
                comboGioiTinh.setSelectedItem(tableMain.getValueAt(selectedRow, 4).toString());
            if(tableMain.getValueAt(selectedRow, 5) != null && !tableMain.getValueAt(selectedRow, 5).toString().equals(""))
                txtNgaySinh.setText(ChuanHoa.chuanHoaNguoc(tableMain.getValueAt(selectedRow, 5).toString()));
            if(tableMain.getValueAt(selectedRow, 6) != null && !tableMain.getValueAt(selectedRow, 6).toString().equals(""))
                txtEmail.setText(tableMain.getValueAt(selectedRow, 6).toString());
        }
    }//GEN-LAST:event_tableMainMouseClicked

