    public void showInfo() {
        txtUserID.setText(acc.getUserID() + "");
        txtTenDangNhap.setText(acc.getTenDangNhap());
        txtMatKhau.setText(acc.getMatKhau());
        
        DocGiaDAO docGiaDAO = new DocGiaDAO();
        if(docGiaDAO.thongTinCaNhan(acc.getUserID()) != null) {
            DocGia dg = docGiaDAO.thongTinCaNhan(acc.getUserID());
            if(dg.getHoTen() != null && !dg.getHoTen().equals(""))
                txtHoTen.setText(dg.getHoTen());
            if(dg.getGioiTinh() != null && !dg.getGioiTinh().equals(""))
                comboGioiTinh.setSelectedItem(dg.getGioiTinh());
            if(dg.getNgaySinh() != null && !dg.getNgaySinh().equals(""))
                txtNgaySinh.setText(ChuanHoa.chuanHoaNguoc(dg.getNgaySinh()));
            if(dg.getDoiTuong() != null /*&& !dg.getDoiTuong().equals("")*/ )
                comboDoiTuong.setSelectedItem(dg.getDoiTuong());
            if(dg.getEmail()!= null && !dg.getEmail().equals(""))
                txtEmail.setText(dg.getEmail());
        }
        
    }
    
