    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String tenDangNhap = txtTenDangNhap.getText();
        String matKhau = String.valueOf(txtMatKhau.getPassword());
        String nhapLaiMatKhau = String.valueOf(txtNhapLaiMatKhau.getPassword());
        int index = comboVaiTro.getSelectedIndex();
        String vaiTro = comboVaiTro.getItemAt(index);

        int luaChon = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn sửa ?");
        if(luaChon == 0) {
            if(matKhau.equals(nhapLaiMatKhau)) {
                TaiKhoan tk = new TaiKhoan(tenDangNhap, matKhau, vaiTro);
                if(new TaiKhoanDAO().updateTaiKhoan(tk, userID)) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công !");
                    clearData();
                    showTable();
                    showSoLuongTaiKhoan();
                    
                    if(new TaiKhoanDAO().timTaiKhoan(tenDangNhap) != null) {
                        TaiKhoan account = new TaiKhoanDAO().timTaiKhoan(tenDangNhap);
                        new DocGiaDAO().updateDocGiaAfteraddTK(account.getTenDangNhap(), account.getUserID());
                    }
                }
                else {
                    JOptionPane.showMessageDialog(this, "Lỗi, vui lòng thử lại !");
                }
            }
            else {
                JOptionPane.showMessageDialog(this, "Lỗi, mật khẩu không khớp !");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

