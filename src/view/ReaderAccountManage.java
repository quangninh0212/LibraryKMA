    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        // TODO add your handling code here:\
        if(acc.getVaiTro().equals("Admin")) {
            Home home = new Home(acc);
            home.setVisible(true);
            home.setLocationRelativeTo(null);
            this.setVisible(false);
        }
        else if(acc.getVaiTro().equals("Thủ thư")) {
            HomeOfLibrarian home = new HomeOfLibrarian(acc);
            home.setVisible(true);
            home.setLocationRelativeTo(null);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnExitActionPerformed

    private void tableMainMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMainMouseClicked
        // TODO add your handling code here:
        int selectedRow = tableMain.getSelectedRow();
        if (selectedRow != -1) {
            txtTenDangNhap.setText(tableMain.getValueAt(selectedRow, 1).toString());
            txtMatKhau.setText(tableMain.getValueAt(selectedRow, 2).toString());

            comboVaiTro.setSelectedItem(tableMain.getValueAt(selectedRow, 3));

            userID = (int)tableMain.getValueAt(selectedRow, 0);
        }
    }//GEN-LAST:event_tableMainMouseClicked

