    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String tenDangNhap = txtTenDangNhap.getText();
        String matKhau = String.valueOf(txtMatKhau.getPassword());
        String nhapLaiMatKhau = String.valueOf(txtNhapLaiMatKhau.getPassword());
        int index = comboVaiTro.getSelectedIndex();
        String vaiTro = comboVaiTro.getItemAt(index);
        if(matKhau.equals(nhapLaiMatKhau)) {
            TaiKhoan tk = new TaiKhoan(tenDangNhap, matKhau, vaiTro);
            if(new TaiKhoanDAO().addTaiKhoan(tk)) {
                JOptionPane.showMessageDialog(this, "Thêm thành công !");
                clearData();
                showTable();
                showSoLuongTaiKhoan();
                
                if(new TaiKhoanDAO().timTaiKhoan(tenDangNhap) != null) {
                    TaiKhoan account = new TaiKhoanDAO().timTaiKhoan(tenDangNhap);
                    new DocGiaDAO().addDocGiaAfteraddTK(account.getTenDangNhap(), account.getUserID());
                }
            }
            else {
                JOptionPane.showMessageDialog(this, "Lỗi, vui lòng thử lại !");
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Lỗi, mật khẩu không khớp !");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int luaChon = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá ?");
        if(luaChon == 0) {
            int possition = tableMain.getSelectedRow();
            int userID = (int)tableMain.getModel().getValueAt(possition, 0);
            TaiKhoanDAO taiKhoanDAO = new TaiKhoanDAO();
            if(new DocGiaDAO().deleteDocGia2(userID) && taiKhoanDAO.deleteTaiKhoan(userID))
                JOptionPane.showMessageDialog(this, "Xoá thành công !");
            else
                JOptionPane.showMessageDialog(this, "Lỗi, vui lòng thử lại !");
            showTable();
            showSoLuongTaiKhoan();
            clearData();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

